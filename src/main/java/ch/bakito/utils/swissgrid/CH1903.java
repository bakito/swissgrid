package ch.bakito.utils.swissgrid;

public class CH1903 {
	private final int x;
	private final int y;

	public CH1903(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	@Override
	public String toString() {
		return "CH1903 [x=" + x + ", y=" + y + "]";
	}

}
